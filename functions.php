//親ページのIDを取得
function get_parent($postID) {
	$p = get_post($postID);
	if ($p->post_parent) {
		return get_parent($p->post_parent);
	} else {
		return $postID;
	}
}

//親カテゴリのIDを取得
function get_parent_category($catID) {
	$c = get_category($catID);
	if ($c->category_parent) {
		return get_parent_category($c->category_parent);
	} else {
		return $catID;
	}
}

//プロジェクトのリダイレクト
function get_project_url($url) {
	global $wp;
	
	$req = explode('/', $wp->request);
	
	if ($req[0] == 'project') {
		$url = str_replace('service', 'project', $url);
	}
	
	return $url;
}
add_filter('redirect_canonical', 'get_project_url', 10, 1);

