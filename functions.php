/* -------------------------------------------------

          URI関連
          get_uri_segment() : URIをセグメント毎に取得
          get_section() : 現在のセクション（1番目のURIセグメント）を取得
          is_section($sec) : 引数が現在のセクションかどうかをbooleanで返す
          set_section() : セクションを取得して定数にセットする

------------------------------------------------- */

function get_uri_segment($segment = 0) {
     global $wp;
     $uri = explode('/', $wp->request);
     return (isset($uri[$segment])) ? $uri[$segment] : false;
}

function get_section() {
     return (defined('SECTION')) ? SECTION : false;
}
function is_section($sec) {
     return ($sec === SECTION && defined('SECTION')) ? true:false;
}
function set_section() {
     define('SECTION', get_uri_segment());
}
add_action('wp', 'set_section');

































